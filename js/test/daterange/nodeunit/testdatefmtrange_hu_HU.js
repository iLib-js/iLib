    testDateRngFmtHURangeinDayShort: function(test) {
        test.expect(2);
        var fmt = new DateRngFmt({locale: "hu-HU", length: "short"});
        test.ok(fmt !== null);
        
        var start = new GregorianDate({
            year: 2011,
            month: 12,
            day: 31,
            hour: 13,
            minute: 45,
            second: 0,
            millisecond: 0
        });
        var end = new GregorianDate({
            year: 2011,
            month: 12,
            day: 31,
            hour: 14,
            minute: 30,
            second: 0,
            millisecond: 0
        });
        test.equal(fmt.format(start, end), "2011. 12. 31. 13:45 – 14:30");
        test.done();
    },
